import type { AppConfig } from '../models/config.js';
import type { GeneratedDataset, GenerationContext, WorkItemDraft, WorkItemRelationDraft } from '../models/generator.js';
import { createSeededRng } from '../utils/rng.js';

const actionWords = ['Implement', 'Investigate', 'Fix', 'Refactor', 'Optimize', 'Validate'];
const domainWords = ['applicant profile sync', 'eligibility rules pipeline', 'assignment notification flow', 'import reconciliation', 'compliance checks', 'workflow transition handling'];
const qualifiers = ['for enterprise onboarding', 'during high-volume processing', 'across integration boundaries', 'for regional rollout', 'under retry conditions', 'for migration readiness'];
const statesByType: Record<string, string[]> = {
  Epic: ['To Do'],
  Feature: ['To Do'],
  'User Story': ['To Do'],
  'Product Backlog Item': ['To Do'],
  Task: ['To Do'],
  Bug: ['To Do'],
  Issue: ['To Do'],
};

const markerDescription = '\n\nSeed Marker: Generated by migration seeder CLI.';

const chooseBacklogType = (availableTypes: string[]): string | undefined => {
  if (availableTypes.includes('User Story')) {
    return 'User Story';
  }
  if (availableTypes.includes('Product Backlog Item')) {
    return 'Product Backlog Item';
  }
  return undefined;
};

const makeTitle = (index: number, rand: ReturnType<typeof createSeededRng>): string => {
  return `${rand.pick(actionWords)} ${rand.pick(domainWords)} ${rand.pick(qualifiers)} (#${index})`;
};

const makeDescription = (title: string, rand: ReturnType<typeof createSeededRng>): string => {
  const acceptanceCriteria = [
    `- Ensure telemetry captures transitions for ${rand.pick(domainWords)}.`,
    `- Confirm handling remains deterministic with seed replay.`,
    `- Validate no duplicate side effects appear after reassignment.`,
  ];

  return [
    `Scenario Summary: ${title}`,
    '',
    `Context: This item models realistic migration source data for ${rand.pick(domainWords)}.`,
    'Expected Outcome: Preserve field fidelity, relations, and metadata across migration.',
    '',
    'Acceptance Criteria:',
    ...acceptanceCriteria,
  ].join('\n');
};

const makeTags = (markerTag: string, enableTags: boolean, rand: ReturnType<typeof createSeededRng>): string[] => {
  const pool = ['MigrationSeed', 'DataQuality', 'Integration', 'RegressionCoverage', 'Compliance'];
  if (!enableTags) {
    return [markerTag];
  }
  const selected = new Set<string>([markerTag, rand.pick(pool)]);
  if (rand.next() > 0.4) {
    selected.add(rand.pick(pool));
  }
  return [...selected];
};

const makeItem = (logicalId: string, type: string, markerTag: string, enableTags: boolean, rand: ReturnType<typeof createSeededRng>, titleIndex: number): WorkItemDraft => {
  const title = makeTitle(titleIndex, rand);
  const stateOptions = statesByType[type] ?? ['To Do'];
  return {
    logicalId,
    type,
    title,
    state: rand.pick(stateOptions),
    description: `${makeDescription(title, rand)}${markerDescription}`,
    tags: makeTags(markerTag, enableTags, rand),
    comments: [
      'Initial migration test-data seed entry created for baseline validation.',
      'Follow-up note: verify relationship mapping in downstream migration run.',
    ],
  };
};

export const generateDataset = (config: AppConfig, context: GenerationContext): GeneratedDataset => {
  const rand = createSeededRng(config.seed);
  const items: WorkItemDraft[] = [];
  const relations: WorkItemRelationDraft[] = [];
  const skippedTypes: string[] = [];

  const backlogType = chooseBacklogType(context.availableTypes);
  const requiredTypes = ['Epic', 'Feature', 'Task', 'Bug', 'Issue'];
  for (const typeName of requiredTypes) {
    if (!context.availableTypes.includes(typeName)) {
      skippedTypes.push(typeName);
    }
  }
  if (!backlogType) {
    skippedTypes.push('User Story/Product Backlog Item');
  }

  let index = 1;

  const epics: string[] = [];
  if (context.availableTypes.includes('Epic')) {
    for (let e = 0; e < config.dataset.epics; e += 1) {
      const logicalId = `epic-${e + 1}`;
      items.push(makeItem(logicalId, 'Epic', context.markerTag, config.options.createTags, rand, index));
      epics.push(logicalId);
      index += 1;
    }
  }

  const features: string[] = [];
  if (context.availableTypes.includes('Feature')) {
    for (const epicId of epics) {
      for (let f = 0; f < config.dataset.featuresPerEpic; f += 1) {
        const logicalId = `feature-${features.length + 1}`;
        const feature = makeItem(logicalId, 'Feature', context.markerTag, config.options.createTags, rand, index);
        feature.parentLogicalId = epicId;
        items.push(feature);
        relations.push({ sourceLogicalId: logicalId, targetLogicalId: epicId, relationType: 'parent' });
        features.push(logicalId);
        index += 1;
      }
    }
  }

  const stories: string[] = [];
  if (backlogType) {
    for (const featureId of features) {
      for (let s = 0; s < config.dataset.userStoriesPerFeature; s += 1) {
        const logicalId = `story-${stories.length + 1}`;
        const story = makeItem(logicalId, backlogType, context.markerTag, config.options.createTags, rand, index);
        story.parentLogicalId = featureId;
        items.push(story);
        relations.push({ sourceLogicalId: logicalId, targetLogicalId: featureId, relationType: 'parent' });
        stories.push(logicalId);
        index += 1;
      }
    }
  }

  if (context.availableTypes.includes('Task')) {
    const taskParents = stories.length > 0 ? stories : epics;
    for (const parentId of taskParents) {
      for (let t = 0; t < config.dataset.tasksPerUserStory; t += 1) {
        const logicalId = `task-${items.filter((item) => item.type === 'Task').length + 1}`;
        const task = makeItem(logicalId, 'Task', context.markerTag, config.options.createTags, rand, index);
        task.parentLogicalId = parentId;
        items.push(task);
        relations.push({ sourceLogicalId: logicalId, targetLogicalId: parentId, relationType: 'parent' });
        index += 1;
      }
    }
  }

  if (context.availableTypes.includes('Bug')) {
    for (let b = 0; b < config.dataset.bugs; b += 1) {
      const logicalId = `bug-${b + 1}`;
      items.push(makeItem(logicalId, 'Bug', context.markerTag, config.options.createTags, rand, index));
      index += 1;
    }
  }

  if (context.availableTypes.includes('Issue')) {
    for (let i = 0; i < config.dataset.issues; i += 1) {
      const logicalId = `issue-${i + 1}`;
      items.push(makeItem(logicalId, 'Issue', context.markerTag, config.options.createTags, rand, index));
      index += 1;
    }
  }

  if (config.options.createRelatedLinks && items.length > 3) {
    const maxLinks = Math.min(5, Math.floor(items.length / 4));
    for (let i = 0; i < maxLinks; i += 1) {
      const source = rand.pick(items);
      const target = rand.pick(items);
      if (source.logicalId !== target.logicalId) {
        relations.push({
          sourceLogicalId: source.logicalId,
          targetLogicalId: target.logicalId,
          relationType: 'related',
        });
      }
    }
  }

  return { items, relations, skippedTypes };
};
